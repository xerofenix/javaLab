public class w3q2_sq_cube {
    public static void main(String[] args) {
        System.out.println("Squares of the numbers 1 to 5");
        for (int i = 1; i < 6; i++) {
            System.out.println(Math.pow(i,2));
        }

        System.out.println("Cubes of the numbers 1 to 5");
        for (int i = 1; i < 6; i++) {
            System.out.println(Math.pow(i,3));
        }
    }
}
